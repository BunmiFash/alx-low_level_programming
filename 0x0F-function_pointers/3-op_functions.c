#include "3-calc.h"

/**
 * op_add - adds two integers
 *
 * @a: First integer
 *
 * @b: Second integer
 *
 * Return: Result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two integers
 *
 * @a: First integer
 *
 * @b: Second integer
 *
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two integers
 *
 * @a: First integer
 *
 * @b: Second integer
 *
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two integers
 *
 * @a: First integer
 *
 * @b: Second integer
 *
 * Return: Result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder of a division
 *
 * @a: First integer
 *
 * @b: second integer
 *
 * Return: Result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
